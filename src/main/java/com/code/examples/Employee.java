package com.code.examples;

class Employee {
  private int id;
  private String name;

  public Employee() {
  }

  public int getId() {
    return id;
  }

  public void setId(final int empId) {
    id = empId;
  }

  public String getName() {
    return name;
  }

  public void setName(final String empName) {
    name = empName;
  }
}
